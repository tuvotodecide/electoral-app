export const getInputBackground = colors =>
  colors.dark ? colors.grayScale800 : colors.inputBackground;

export const getBorderColor = colors =>
  colors.dark ? colors.grayScale600 : colors.grayScale300;

export const getSecondaryTextColor = colors =>
  colors.dark ? colors.grayScale400 : colors.grayScale600;

export const getDisableTextColor = colors =>
  colors.dark ? colors.grayScale200 : colors.grayScale700;
